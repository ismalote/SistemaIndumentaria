package main.java.vista;

public class PrendaSinTemporadaView {
	
	private String nombrePrenda;
	private int stock;
	
	public PrendaSinTemporadaView(String nombrePrenda, int stock) {
		this.nombrePrenda = nombrePrenda;
	}

	public String getNombrePrenda() {
		return nombrePrenda;
	}

	public int getStock() {
		return stock;
	}
	
	
}
